mood = input("How are we feeling today?: ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up, mate!")
elif mood == "excited":
    print("Hold your horses, mister!")
elif mood == "relaxed":
    print("Get going, mate!")
else:
    print("I don't recognize this mood")
